int	ft_sqrt(int nb)
{
	int pow;

	pow = 1;
	if (nb <= 0)
		return (0);
	while (pow * pow < nb)
	{
		pow++;
	}
	if (pow * pow == nb)
		return (pow);
	else
		return (0);
}
