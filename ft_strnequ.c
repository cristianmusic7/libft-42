#include <libft.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t c;

	c = 0;
	if (s1 && s2)
	{
		while (s1[c] != '\0' && c < n)
		{
			if (s1[c] != s2[c])
				return (0);
			c++;
		}
		if (n == 0 || s1[c - 1] == s2[c - 1])
			return (1);
	}
	return (0);
}
