int		ft_strcmp(char *s1, char *s2)
{
	int c;

	c = 0;
	while (s1[c] != '\0')
	{
		if (s1[c] != s2[c])
			break ;
		c++;
	}
	return ((unsigned char)s1[c] - (unsigned char)s2[c]);
}
